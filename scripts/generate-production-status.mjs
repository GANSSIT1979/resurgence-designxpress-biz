import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const outputPath = path.join(root, 'docs', 'PRODUCTION_STATUS.md')
const mode = process.argv.includes('--check') ? 'check' : 'write'
const siteUrl = process.env.PRODUCTION_SITE_URL || 'https://www.resurgence-dx.biz'
const healthUrl = new URL('/api/health', siteUrl).toString()

async function fetchJson(url) {
  const response = await fetch(url, {
    headers: {
      accept: 'application/json',
      'user-agent': 'resurgence-production-status-check/1.0',
    },
  })

  const text = await response.text()
  let data = null

  try {
    data = text ? JSON.parse(text) : null
  } catch {
    throw new Error(`Expected JSON from ${url}, received: ${text.slice(0, 200)}`)
  }

  if (!response.ok) {
    throw new Error(`Health request failed with ${response.status}: ${JSON.stringify(data)}`)
  }

  return data
}

function assertHealthy(health) {
  const failures = []

  if (!health?.ok) failures.push('health.ok is not true')
  if (health?.status !== 'ok') failures.push(`health.status is ${JSON.stringify(health?.status)}`)
  if (health?.database !== 'connected') failures.push(`database is ${JSON.stringify(health?.database)}`)
  if (health?.schema?.status !== 'ok') failures.push(`schema.status is ${JSON.stringify(health?.schema?.status)}`)
  if (Array.isArray(health?.schema?.issues) && health.schema.issues.length > 0) {
    failures.push(`schema has issues: ${health.schema.issues.join(', ')}`)
  }

  if (failures.length) {
    throw new Error(`Production health check failed:\n- ${failures.join('\n- ')}`)
  }
}

function renderStatus(health) {
  const generatedAt = new Date().toISOString()
  const support = health.support || {}
  const counts = health.counts || {}

  return `# Production Status

Updated: ${generatedAt}

This document is generated by \`scripts/generate-production-status.mjs\`.

## Live URLs

| Surface | URL | Status |
|---|---|---|
| Canonical site | ${siteUrl} | Live |
| Health endpoint | ${healthUrl} | Healthy |

## Health Summary

| Check | Value |
|---|---|
| App OK | ${String(Boolean(health.ok))} |
| Status | ${health.status || 'unknown'} |
| Database | ${health.database || 'unknown'} |
| Schema | ${health.schema?.status || 'unknown'} |
| AI configured | ${String(Boolean(health.aiConfigured))} |
| ChatKit ready | ${String(Boolean(support.chatkitReady))} |
| Webhook ready | ${String(Boolean(support.webhookReady))} |

## Counts

| Entity | Count |
|---|---:|
| Users | ${Number.isFinite(counts.users) ? counts.users : 'unknown'} |
| Sponsors | ${Number.isFinite(counts.sponsors) ? counts.sponsors : 'unknown'} |
| Packages | ${Number.isFinite(counts.packages) ? counts.packages : 'unknown'} |

## Support Configuration

| Setting | Ready |
|---|---|
| API key configured | ${String(Boolean(support.apiKeyConfigured))} |
| Workflow ID configured | ${String(Boolean(support.workflowIdConfigured))} |
| Workflow version configured | ${String(Boolean(support.workflowVersionConfigured))} |
| Prompt ID configured | ${String(Boolean(support.promptIdConfigured))} |
| Prompt version configured | ${String(Boolean(support.promptVersionConfigured))} |
| Webhook secret configured | ${String(Boolean(support.webhookSecretConfigured))} |

## Schema Issues

${health.schema?.issues?.length ? health.schema.issues.map((issue) => `- ${issue}`).join('\n') : '- None'}

## Verification Commands

\`\`\`bash
curl -I ${siteUrl}
curl ${healthUrl}
\`\`\`

## Runtime Environment Requirements

Production and Preview must include:

\`\`\`env
PRISMA_DB_PROVIDER=postgresql
DATABASE_URL=postgresql://POOLER_USER:POOLER_PASSWORD@POOLER_HOST:6543/postgres?sslmode=require&pgbouncer=true
DIRECT_URL=postgresql://DIRECT_USER:DIRECT_PASSWORD@DIRECT_HOST:5432/postgres?sslmode=require
JWT_SECRET=...
NEXT_PUBLIC_SITE_URL=${siteUrl}
FORCE_HTTPS=true
\`\`\`

## Log Interpretation

- Ignore old \`DATABASE_URL\` errors when the log filter is pinned to old deployment IDs.
- Only investigate errors from the active production deployment.
- \`POST /api/openai/webhook\` may return \`400\` for unsigned, malformed, or manual test requests.
- A webhook \`400\` is only actionable if it comes from a real OpenAI webhook delivery using the configured production secret.
`
}

const health = await fetchJson(healthUrl)
assertHealthy(health)
const nextContent = renderStatus(health)

if (mode === 'check') {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : ''
  const normalize = (value) => value.replace(/^Updated: .*$/m, 'Updated: <generated>').trim()

  if (normalize(current) !== normalize(nextContent)) {
    console.error('docs/PRODUCTION_STATUS.md is out of date. Run: npm run docs:production-status')
    process.exit(1)
  }

  console.log('[production-status] docs/PRODUCTION_STATUS.md is current')
} else {
  fs.writeFileSync(outputPath, nextContent)
  console.log(`[production-status] wrote ${path.relative(root, outputPath)}`)
}
